<?php
namespace I95dev\Phpunittesting\Model;
 
class Arithmetic
{
   
    public function add($a, $b)
    {
        return $a + $b;
    }

  
    public function subtract($a, $b)
    {
        return $a - $b;
    }

   
    public function multiply($a, $b)
    {
        return $a * $b;
    }

 
    public function divide($a, $b)
    {
        return $a / $b;
    }
}
